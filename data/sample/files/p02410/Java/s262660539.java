public class Main {
    public static void main(String[] args) {

        // ??????????????????????????????.
        java.util.Scanner sc = new java.util.Scanner(System.in);

        // ??????????????????????????? a ????????°??¨?¨???°???????????????.
        // ?????? a ???????????????.
        int tate = sc.nextInt();
        int yoko = sc.nextInt();
        int[][] A = new int[tate][yoko];
        for (int i = 0; i < tate; i ++) {
            for (int j = 0; j < yoko; j ++) { A[i][j] = sc.nextInt(); }
        }

        // ?????? b ???????????????.
        int[] b = new int[yoko];
        for (int i = 0; i < yoko; i ++) { b[i] = sc.nextInt(); }

        // ???????????????????????????.
        for (int i = 0; i < tate; i ++) {
            int sum = 0;
            for (int j = 0; j < yoko; j ++) { sum += b[j] * A[i][j]; }
            System.out.print(sum + "\n");
        }
    }
}